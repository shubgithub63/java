public class whileloop {
    public static void main(String[]args){
        //print 1 to 10
        int i=0;
        while(i<11){
            System.out.println(i);
            i=i+1;
        }
            //print 0 to 10 by do while
           int j=0;
            do{
                System.out.println(j);
                j++;
            }while(j<11);
        
    }
}
